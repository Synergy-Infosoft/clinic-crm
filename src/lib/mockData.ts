const makeInvoice = (
  id: string,
  visitId: string,
  patientId: string,
  invoiceNumber: string,
  items: LineItem[],
  discount: number,
  paymentStatus: 'pending' | 'paid_cash' | 'paid_online',
  paymentMethod: 'cash' | 'online_upi' | null,
  createdAt: string,
  paidAt: string | null,
  visit?: Visit,
  patient?: Patient
): Invoice => {
  const subtotal = items.reduce((acc, item) => acc + item.amount * item.quantity, 0)
  const total = subtotal - discount
  return {
    id,
    visit_id: visitId,
    patient_id: patientId,
    invoice_number: invoiceNumber,
    line_items: items,
    subtotal,
    discount,
    total,
    payment_status: paymentStatus,
    payment_method: paymentMethod,
    paid_at: paidAt,
    created_at: createdAt,
    visit,
    patient,
  }
}

